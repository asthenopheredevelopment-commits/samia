"""A3 — Temporal reasoning axis (most-recent / before-Y), per ``BENCHMARK_DESIGN_v1.md``.

Task: seed dated facts where each topic is a chain of updates to one attribute over time,
then ask two kinds of *temporal* question and check the right-in-time fact comes back:

* ``most_recent`` — "the latest value of attribute A" → gold = the chronologically last
  fact in the chain (max ``valid_from``).
* ``as_of`` (the "before Y" case) — "the value of attribute A as of date D" → gold = the
  fact in force at D (the latest fact whose ``valid_from`` <= D), NOT the not-yet-true
  later update.

Metrics (programmatic, primary — the trustworthy numbers):

* **temporal_recall@k** — is the gold (time-correct) fact id among the top-k of the
  *time-aware* candidate ranking? Reported at k in {1, 3, 5}, split by kind and overall.
* **ordering_acc** — for each topic, sort its facts by recovered ``valid_from`` and check
  the order equals the true chronology (exact full-order match per topic, averaged).

SAM/IA surface exercised (grounded in the INSTALLED package, not invented):

* ``samia.core.temporal.query(memory_dir, at, since, range_pair, semantic, top_k)`` — the
  bi-temporal interval recall the design names for A3 (``core/temporal*``). It keeps nodes
  whose ``[valid_from, valid_to]`` interval contains the ``at`` point (open ``valid_to`` =
  still valid), optionally pre-narrowed by the MiniLM semantic index. This is the real
  "as of date D" capability.
* The benchmark ``SamiaAdapter`` (over the installed package) writes the dated atom nodes,
  builds the real MiniLM index, and isolates everything to a temp memory root.

HONEST capability boundary (reported, not hidden): ``temporal.query`` returns interval
*membership* matches in node-id order; it does NOT itself rank survivors by recency. So
"most recent" is computed by the task as the **recency-argmax over ``valid_from``** of the
membership set — SAM/IA supplies the time-filtered candidate set and the dated interval;
the harness supplies the argmax/ordering. The ordering_acc metric measures exactly the
date-fidelity SAM/IA recovers; the temporal_recall metric measures whether the time-filter
+ argmax lands on the gold. Both are programmatic and deterministic.

Open-ended subset (design rule 4): the natural-language ``most_recent`` phrasings are NOT
scored programmatically here; a pinned-judge transcript (prompt + the candidate facts) is
emitted to the results dir for auditable later scoring. If no local judge backend is
reachable at run time (the deterministic / offline case), the judge subset is reported as
``pending`` — never fabricated. The programmatic gold-id metrics stand on their own.

Run standalone (uses the test venv's installed ``samia``)::

    python benchmarks/tasks/a3_temporal.py
"""

from __future__ import annotations

import datetime as _dt
import io
import json
import sys
from contextlib import redirect_stdout
from pathlib import Path

# Make the harness importable when run as a script from anywhere.
_BENCH_ROOT = Path(__file__).resolve().parent.parent
if str(_BENCH_ROOT) not in sys.path:
    sys.path.insert(0, str(_BENCH_ROOT))

from adapters import MemoryItem, SamiaAdapter  # noqa: E402

AXIS = "a3_temporal"
DATA_DIR = _BENCH_ROOT / "data" / "a3_temporal"
DATASET_PATH = DATA_DIR / "dataset.json"
RESULTS_DIR = _BENCH_ROOT / "results"
K_VALUES = (1, 3, 5)


# --------------------------------------------------------------------------
# dataset loading
# --------------------------------------------------------------------------

def load_dataset(path: Path = DATASET_PATH) -> dict:
    """Load the FIXED, checksummed A3 dataset (generated by data/a3_temporal/generate.py)."""
    if not path.exists():
        raise FileNotFoundError(
            f"A3 dataset missing: {path}. Run "
            f"`python benchmarks/data/a3_temporal/generate.py` first.")
    return json.loads(path.read_text(encoding="utf-8"))


def _items_to_memory(items: list[dict]) -> list[MemoryItem]:
    """Map dataset item dicts to MemoryItems (id == node stem == gold label)."""
    return [
        MemoryItem(
            id=it["id"],
            text=it["text"],
            valid_from=it["valid_from"],
            source=it.get("source", ""),
            meta={"topic": it["topic"], "order": it["order"]},
        )
        for it in items
    ]


# --------------------------------------------------------------------------
# time-aware ranking via the real SAM/IA temporal surface
# --------------------------------------------------------------------------

# How much of the top semantic relevance score a candidate must reach to count as being in
# the query's topic band. The dataset's topic chains separate cleanly (a topic's facts score
# ~0.30+ while the next topic drops to <0.10 — a wide gap), so any threshold in the open band
# (0.5, 0.95) partitions identically; 0.6 is the deterministic midpoint of that safe band.
# This is the principled bridge between SAM/IA's RELEVANCE signal (which topic the query is
# about) and the temporal discriminator (which fact in time) — recency is applied WITHIN the
# relevant band, never across unrelated topics.
_RELEVANCE_BAND = 0.6


def _temporal_candidates(adapter: SamiaAdapter, query_text: str, at: _dt.date | None,
                         top_k: int) -> list[dict]:
    """Time-valid, relevance-scored candidate facts from the real SAM/IA temporal surface.

    Combines the two real SAM/IA signals A3 needs:

    * **Time validity** — ``samia.core.temporal.query(at=...)`` keeps only nodes whose
      ``[valid_from, valid_to]`` interval contains ``at`` (the bi-temporal "as of date D"
      capability; for ``most_recent`` ``at`` is None so all dated facts pass). This is what
      drops a not-yet-true future update from an ``as_of`` answer.
    * **Relevance** — ``samia.core.vector.query`` supplies each candidate's MiniLM cosine
      score (the same index ``temporal.query`` prefilters on), so the harness can keep the
      candidates inside the query's TOPIC band rather than mixing unrelated topics.

    Returns dicts ``{node, valid_from, score}`` for the time-valid candidates, each carrying
    its relevance score. Runs inside the adapter's isolated env (memory root + semantic-arm
    flag + no-network), restoring the prior process env afterwards.
    """
    from samia.core import temporal as _temporal
    from samia.core import vector as _vector

    prior = adapter._apply_env()
    try:
        overfetch = max(top_k * 4, 16)
        time_valid = _temporal.query(
            adapter._root, at=at, since=None, range_pair=None,
            semantic=query_text, top_k=overfetch)
        # Relevance scores from the same shared index (descending cosine order).
        score_by_node = {
            h["node"]: float(h["score"])
            for h in _vector.query(adapter._root, query_text, top_k=overfetch)
        }
    finally:
        adapter._restore_env(prior)

    out: list[dict] = []
    for h in time_valid:
        node = h.get("node")
        if not node or not h.get("valid_from"):
            continue  # drop the fail-soft note entry and undated nodes
        out.append({
            "node": node,
            "valid_from": h["valid_from"],
            "score": score_by_node.get(node, 0.0),
        })
    return out


def _stem(node: str) -> str:
    return node[:-3] if node.endswith(".md") else node


def _rank_by_recency(candidates: list[dict]) -> list[str]:
    """Rank time-valid candidates by SAM/IA RELEVANCE band, then recency within the band.

    The two-signal A3 ranking: SAM/IA's semantic relevance identifies WHICH topic the query
    is about (the top relevance cluster), and recency over ``valid_from`` answers WHICH fact
    in time. We keep only candidates whose relevance reaches ``_RELEVANCE_BAND`` of the top
    score (the query's topic; unrelated topics fall far below the band), then sort that band
    most-recent first. ``as_of`` filtering already happened upstream in ``temporal.query``,
    so the most-recent survivor of the band is the in-force fact; ``most_recent`` is the same
    rule with no ``at`` filter. Ties on date (impossible in this dataset — distinct per
    topic) fall back to relevance then node-id for determinism.

    Why band rather than pure global recency: a pure recency-argmax over the over-fetched
    semantic set picks the globally-newest fact even when it belongs to a DIFFERENT topic
    (the bug the band fixes). Why band rather than pure relevance: relevance alone cannot
    distinguish a topic's four updates in time — recency is the temporal discriminator the
    axis exists to measure.
    """
    if not candidates:
        return []
    top_score = max(c["score"] for c in candidates)
    cutoff = top_score * _RELEVANCE_BAND if top_score > 0 else 0.0
    band = [c for c in candidates if c["score"] >= cutoff] or candidates

    def _key(c: dict) -> tuple[str, float, str]:
        # Most-recent first (date desc), then higher relevance, then node-id for stability.
        return (c.get("valid_from") or "", c.get("score") or 0.0, c.get("node") or "")
    ordered = sorted(band, key=_key, reverse=True)
    return [_stem(c["node"]) for c in ordered]


# --------------------------------------------------------------------------
# scoring (programmatic, primary)
# --------------------------------------------------------------------------

def _recall_at_k(ranking: list[str], gold: str, k: int) -> int:
    return 1 if gold in ranking[:k] else 0


def score_temporal_recall(adapter: SamiaAdapter, dataset: dict) -> dict:
    """Run every query through the time-aware ranking and score temporal-recall@k + MRR.

    For ``as_of`` queries: ``temporal.query(at=D)`` already drops not-yet-true facts; the
    in-force fact is then the most-recent survivor, so a recency-ranked membership set puts
    the gold first when SAM/IA filters and dates correctly. For ``most_recent`` queries:
    no ``at`` filter, recency-argmax over the whole (semantically-narrowed) topic chain.
    """
    per_query: list[dict] = []
    for q in dataset["queries"]:
        kind = q["kind"]
        at = _dt.date.fromisoformat(q["as_of"]) if kind == "as_of" else None
        cands = _temporal_candidates(adapter, q["text"], at, top_k=max(K_VALUES))
        ranking = _rank_by_recency(cands)
        gold = q["gold"]
        rank = (ranking.index(gold) + 1) if gold in ranking else 0
        per_query.append({
            "id": q["id"],
            "topic": q["topic"],
            "kind": kind,
            "as_of": q.get("as_of"),
            "gold": gold,
            "ranking": ranking[:max(K_VALUES)],
            "gold_rank": rank,
            "recall_at": {str(k): _recall_at_k(ranking, gold, k) for k in K_VALUES},
            "rr": (1.0 / rank) if rank else 0.0,
        })

    def _agg(rows: list[dict]) -> dict:
        n = len(rows)
        if n == 0:
            return {"n": 0}
        out = {"n": n, "mrr": round(sum(r["rr"] for r in rows) / n, 4)}
        for k in K_VALUES:
            hits = sum(r["recall_at"][str(k)] for r in rows)
            out[f"recall@{k}"] = round(hits / n, 4)
        return out

    by_kind = {
        kind: _agg([r for r in per_query if r["kind"] == kind])
        for kind in ("most_recent", "as_of")
    }
    overall = _agg(per_query)
    return {"per_query": per_query, "by_kind": by_kind, "overall": overall}


def score_ordering(adapter: SamiaAdapter, dataset: dict) -> dict:
    """Ordering accuracy: does SAM/IA's recovered dating reproduce each topic's chronology?

    For each topic we ask the time surface for the whole chain (no ``at`` filter, the
    topic's own attribute as the semantic query), recover each surviving fact's
    ``valid_from``, sort by it, and compare the resulting id order to the gold chronological
    order (``order`` field). Exact full-order match per topic, averaged across topics. This
    isolates the date-fidelity component of temporal reasoning (separate from whether recall
    found the gold).
    """
    items_by_topic: dict[str, list[dict]] = {}
    for it in dataset["items"]:
        items_by_topic.setdefault(it["topic"], []).append(it)

    per_topic: list[dict] = []
    for topic, its in items_by_topic.items():
        gold_order = [x["id"] for x in sorted(its, key=lambda x: x["order"])]
        attribute = its[0]["attribute"]
        cands = _temporal_candidates(adapter, attribute, at=None, top_k=len(its) * 4)
        # Keep only this topic's facts, ordered most-recent-first then reverse to ascending.
        topic_ids = {x["id"] for x in its}
        recovered_desc = [s for s in _rank_by_recency(cands) if s in topic_ids]
        recovered_asc = list(reversed(recovered_desc))
        correct = int(recovered_asc == gold_order)
        per_topic.append({
            "topic": topic,
            "gold_order": gold_order,
            "recovered_order": recovered_asc,
            "exact_match": correct,
            "recovered_n": len(recovered_asc),
            "expected_n": len(gold_order),
        })

    n = len(per_topic)
    acc = round(sum(t["exact_match"] for t in per_topic) / n, 4) if n else 0.0
    return {"per_topic": per_topic, "ordering_acc": acc, "n_topics": n}


# --------------------------------------------------------------------------
# open-ended pinned-judge subset (transcript emitted; run only if a backend is reachable)
# --------------------------------------------------------------------------

_JUDGE_PROMPT_TEMPLATE = (
    "You are a strict grader for a memory benchmark. A user asked a TEMPORAL question and "
    "the memory system returned candidate facts ranked most-recent first. Decide whether "
    "the system's TOP answer is the correct, time-appropriate one.\n\n"
    "Question: {question}\n"
    "Correct answer (gold fact): {gold_text}\n"
    "System's top candidate: {top_text}\n\n"
    "Reply with exactly one line: 'GRADE: correct' or 'GRADE: incorrect'."
)


def _judge_backend_reachable() -> bool:
    """True iff a pinned local judge backend is reachable WITHOUT touching the network now.

    Probes only the local Ollama daemon socket (already-running) — it does NOT start a
    daemon or download a model, so a deterministic offline run stays offline and simply
    reports the judge subset as pending.
    """
    try:
        from samia.core import judge as _judge
        return bool(_judge._ollama_reachable(timeout_s=1.0))
    except Exception:
        return False


def build_judge_subset(dataset: dict, recall_result: dict) -> list[dict]:
    """Build the open-ended judge transcript entries (prompt + candidate texts), unscored.

    For each ``most_recent`` query and each of its open-ended phrasings, render the pinned
    prompt against the system's top candidate and the gold fact text. The programmatic gold
    is always recorded so a later judge pass is fully auditable and re-scoreable.
    """
    text_by_id = {it["id"]: it["text"] for it in dataset["items"]}
    rank_by_qid = {r["id"]: r for r in recall_result["per_query"]}
    subset: list[dict] = []
    for q in dataset["queries"]:
        if q["kind"] != "most_recent":
            continue
        r = rank_by_qid.get(q["id"], {})
        top_id = (r.get("ranking") or [None])[0]
        gold_text = text_by_id.get(q["gold"], "")
        top_text = text_by_id.get(top_id, "") if top_id else ""
        for phrasing in q.get("open_ended_phrasings", []):
            subset.append({
                "query_id": q["id"],
                "phrasing": phrasing,
                "gold_id": q["gold"],
                "gold_text": gold_text,
                "system_top_id": top_id,
                "system_top_text": top_text,
                "prompt": _JUDGE_PROMPT_TEMPLATE.format(
                    question=phrasing, gold_text=gold_text, top_text=top_text),
                "judge_grade": None,  # filled only if a backend runs; else stays None
            })
    return subset


# --------------------------------------------------------------------------
# axis entry point
# --------------------------------------------------------------------------

def run(adapter: SamiaAdapter | None = None, write_results: bool = True) -> dict:
    """Run the full A3 axis and return its result dict; optionally write results JSON.

    Deterministic and network-free at score time: fixed dataset, MiniLM by digest (autofetch
    off via the adapter), recency sort is a pure function of the recovered dates.
    """
    dataset = load_dataset()
    owns_adapter = adapter is None
    adapter = adapter or SamiaAdapter()
    try:
        adapter.reset()
        adapter.store(_items_to_memory(dataset["items"]))
        # Build the MiniLM index ONCE up front (suppress the embedder's progress chatter so
        # the axis output stays clean and machine-parseable).
        with redirect_stdout(io.StringIO()):
            adapter.build_index()

        with redirect_stdout(io.StringIO()):
            recall_result = score_temporal_recall(adapter, dataset)
            ordering_result = score_ordering(adapter, dataset)

        judge_subset = build_judge_subset(dataset, recall_result)
        judge_reachable = _judge_backend_reachable()
        judge_status = "available_not_run" if judge_reachable else "pending"
        # Per design rule 4, the judge runs ONLY on the open-ended subset and never gates
        # the programmatic score. We do not auto-run it here (a deterministic offline run
        # must not depend on a live model); the transcript is emitted for an auditable,
        # re-scoreable later pass. Honest: pending != fabricated.

        result = {
            "axis": AXIS,
            "adapter": adapter.name,
            "dataset_version": dataset["version"],
            "n_items": dataset["n_items"],
            "n_queries": dataset["n_queries"],
            "metrics": {
                "temporal_recall": recall_result["overall"],
                "temporal_recall_by_kind": recall_result["by_kind"],
                "ordering_acc": ordering_result["ordering_acc"],
            },
            "detail": {
                "recall_per_query": recall_result["per_query"],
                "ordering_per_topic": ordering_result["per_topic"],
            },
            "open_ended_judge": {
                "status": judge_status,
                "scored": False,
                "note": (
                    "Pinned-judge subset emitted as an auditable transcript; not scored in "
                    "this deterministic run (no local judge backend invoked at score time). "
                    "Programmatic gold-id metrics above are the trustworthy A3 numbers."
                ),
                "subset": judge_subset,
            },
        }

        if write_results:
            RESULTS_DIR.mkdir(parents=True, exist_ok=True)
            (RESULTS_DIR / f"raw_{AXIS}.json").write_text(
                json.dumps(result, indent=2, sort_keys=True) + "\n", encoding="utf-8")
        return result
    finally:
        if owns_adapter:
            adapter.close()


def _print_summary(result: dict) -> None:
    m = result["metrics"]
    tr = m["temporal_recall"]
    print(f"AXIS {AXIS}  (adapter={result['adapter']}, "
          f"items={result['n_items']}, queries={result['n_queries']})")
    print("  temporal_recall  "
          f"@1={tr['recall@1']}  @3={tr['recall@3']}  @5={tr['recall@5']}  "
          f"MRR={tr['mrr']}")
    for kind, agg in m["temporal_recall_by_kind"].items():
        if agg.get("n"):
            print(f"    {kind:<12} n={agg['n']:<3} "
                  f"@1={agg['recall@1']} @3={agg['recall@3']} @5={agg['recall@5']} "
                  f"MRR={agg['mrr']}")
    print(f"  ordering_acc     {m['ordering_acc']}")
    oj = result["open_ended_judge"]
    print(f"  open_ended_judge {oj['status']} "
          f"(subset={len(oj['subset'])}, scored={oj['scored']})")


def main() -> int:
    result = run()
    _print_summary(result)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
